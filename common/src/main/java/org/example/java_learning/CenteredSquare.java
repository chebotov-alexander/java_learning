package org.example.java_learning;

public class CenteredSquare implements Shape {
    private double centerCoordinatesX;
    private double centerCoordinatesY;
    private double size;

    public CenteredSquare(double centerCoordinatesX, double centerCoordinatesY, double size) {
        this.centerCoordinatesX = centerCoordinatesX;
        this.centerCoordinatesY = centerCoordinatesY;
        this.size = size;
    }
}
